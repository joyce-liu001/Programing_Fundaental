#include <stdio.h>

int main(void) {

    //initialise counter to 10
    int counter = 10; 
    // loop until not <= 0
    while (counter >= 0) { // Have printed all numbers between 1 and counter
        // print counter
        printf("%d\n", counter);
        // increment counter
        counter = counter - 1; 
    } 


}
